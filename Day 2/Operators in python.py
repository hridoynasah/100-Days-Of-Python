# Arithmetic Operators
a = 10
b = 3

print(a + b)  # Output: 13
print(a - b)  # Output: 7
print(a * b)  # Output: 30
# floating point division
print(a / b)  # Output: 3.3333333333333335
# Integer division or Floor division
print(a // b)  # Output: 3
print(a % b)  # Output: 1
# Power : a to the power b
print(a ** b)  # Output: 1000

# Assignment Operators
x = 5
x += 3  # x is now 8
x -= 2  # x is now 6
x *= 4  # x is now 24
x /= 3  # x is now 8.0
x //= 2  # x is now 4.0
x %= 3  # x is now 1.0
x **= 2  # x is now 1.0

# Unary Operators
y = -10
print(+y)  # Output: -10
print(-y)  # Output: 10

print(3 * 3 / 3 + 3 - 3)

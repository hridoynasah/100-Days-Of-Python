# You will live until 90 years
# One year = 365 days , 52 weeks and 12 months
# Calculate how many days , weeks and months you are gonna live

current_age = int(input("What is your current age? "))
remaining_age = 90 - current_age
total_days = remaining_age * 365
total_weeks = remaining_age * 52
total_months = remaining_age * 12
print(f"You have {total_days} or {total_weeks} weeks or {total_months} months left.")



# Primitive data types in python :
# Integers, Floating point, Boolean, String, NoneType
# String Data type : Immutable data type
print("Hello"[3])
print("Hello"[0])
print("Hello"[2])

# Integer data type : Whole value
x = 10
print(x)

# Float data type : Decimal point value
f = 10.546
print(f)

# Boolean data type : True or False
adult = True
print(adult)

# None data type : Null value
hi = None
print(hi)

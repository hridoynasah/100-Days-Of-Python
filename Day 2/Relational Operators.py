# Define some variables
x = 10
y = 20
z = 10

# Equal to
result_equal = (x == y)
print(f"x == y: {result_equal}")  # Output: False (10 is not equal to 20)

# Not equal to
result_not_equal = (x != y)
print(f"x != y: {result_not_equal}")  # Output: True (10 is not equal to 20)

# Greater than
result_greater = (x > y)
print(f"x > y: {result_greater}")  # Output: False (10 is not greater than 20)

# Less than
result_less = (x < y)
print(f"x < y: {result_less}")  # Output: True (10 is less than 20)

# Greater than or equal to
result_greater_equal = (x >= z)
print(f"x >= z: {result_greater_equal}")  # Output: True (10 is equal to 10)

# Less than or equal to
result_less_equal = (x <= z)
print(f"x <= z: {result_less_equal}")  # Output: True (10 is equal to 10)
